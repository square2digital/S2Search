using Nest;
using System.Text;

namespace Domain.Extentions
{
    public static class ElasticExtensions
    {
        public static string GetRequestJson(this IResponse response)
        {
            return Encoding.UTF8.GetString(response.ApiCall.RequestBodyInBytes);
        }

        public static string GetResponseJson(this IResponse response)
        {
            return Encoding.UTF8.GetString(response.ApiCall.ResponseBodyInBytes);
        }
    }
}
